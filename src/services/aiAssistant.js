export async function sendMessage(userMessage, chatHistory, state) {
  const messages = chatHistory.map(m => ({
    role: m.role,
    content: m.content,
  }));
  messages.push({ role: 'user', content: userMessage });

  const reply = await callOpenRouter(messages, state, chatHistory);
  return reply;
}

